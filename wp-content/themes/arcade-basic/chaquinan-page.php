        <p class="red-text">
            Del quichua chaqui: pie, y ñan: camino, el Chaquiñán es un largo sendero que sigue la antigua vía férrea 
            construida a principios del siglo pasado. 
            Este tramo del ferrocarril más difícil del mundo se edificó entre 1918 y 1926 como parte de una ruta mayor 
            que conectaba Quito con Ibarra. Está localizado en el cálido valle de Cumbayá, 
            al nororiente de Quito. 
            El Concejo Metropolitano de Quito declaró como Patrimonio Natural a estos 22 kilómetros, en su categoría 
            Corredor Ecológico y Recreativo.  La mayoría lo recorren en bicicleta, uno de sus mayores atractivos son 
            los varios túneles que se deben cruzar y que fueron también construidos para el paso del tren en el tramo 
            Cumbayá-Tumbaco-Puembo. El más largo de ellos mide 250 metros. 
